bool lgui_pressed = false;
bool lower_pressed = false;
bool both_pressed = false;

bool process_left_alt(uint16_t keycode, keyrecord_t *record) {
  if (lgui_pressed && lower_pressed) {
    //unregister lgui
    unregister_code(KC_LGUI);
    register_code(KC_LALT);
    both_pressed = true;
    return true;
  } else if (both_pressed && lgui_pressed && !lower_pressed) {
    unregister_code(KC_LALT);
    register_code(KC_LGUI);
    both_pressed = false;
    return false;
  } else if (both_pressed && lower_pressed && !lgui_pressed) {
    unregister_code(KC_LALT);
    both_pressed = false;
    return false;
  }
  return false;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case KC_LGUI:
      lgui_pressed = record->event.pressed;
      if (process_left_alt(keycode, record)) {
        return true;
      }
      break;
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      lower_pressed = record->event.pressed;
      if (process_left_alt(keycode, record)) {
        return true;
      }
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
  if (index == 0) { /* First encoder */
    if (clockwise) {
      tap_code(KC_VOLD);
    } else {
      tap_code(KC_VOLU);
    }
  } 
}